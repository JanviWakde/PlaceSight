import numpy as np
import pandas as pd
import pickle
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn import preprocessing
from sklearn.metrics import r2_score, mean_absolute_error, mean_squared_error

from google.colab import drive
drive.mount('/content/drive')

data = pd.read_csv("/content/drive/MyDrive/placement_prediction_data.csv")

print(data)

data.info()

data.shape

data.describe()

"""**Data Cleaning**"""

print(data.isnull().sum())

num_duplicates = data.duplicated().sum()
print(f"Number of duplicate rows: {num_duplicates}")

data.rename(columns={'Communication Skill Rating':'Communication Rating','Workshops/Certificatios':'Workshops','backlogs':'Backlogs','salary':'Salary'},inplace=True)

data = data.loc[:, ~data.columns.str.contains('^Unnamed')]

data

data = data[data['PlacementStatus'] == 'Placed']

X = data.drop(['StudentId', 'Salary'], axis=1)
y = data['Salary']

le = preprocessing.LabelEncoder()

if X['Internship'].dtype == 'object':
    X['Internship'] = le.fit_transform(X['Internship'])

if X['Hackathon'].dtype == 'object':
    X['Hackathon'] = le.fit_transform(X['Hackathon'])

if X['PlacementStatus'].dtype == 'object':
    X['PlacementStatus'] = le.fit_transform(X['PlacementStatus'])

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3, random_state=100)

model = RandomForestRegressor(n_estimators=200, random_state=42)
model.fit(X_train, y_train)

y_pred = model.predict(X_test)

r2 = r2_score(y_test, y_pred)
mae = mean_absolute_error(y_test, y_pred)
mse = mean_squared_error(y_test, y_pred)

print("Model Accuracy (R² Score):", r2)
print("Mean Absolute Error:", mae)
print("Mean Squared Error:", mse)

with open("salary_model.pkl", "wb") as f:
    pickle.dump(model, f)

with open("salary_model.pkl", "rb") as f:
    loaded_model = pickle.load(f)

input_data = pd.DataFrame([[8,1,3,2,9,4.8,0,1,71,87,0,1]],
                          columns=['CGPA', 'Major Projects', 'Workshops', 'Mini Projects',
                                   'Skills', 'Communication Rating', 'Internship', 'Hackathon',
                                   '12th Percentage', '10th Percentage', 'Backlogs', 'PlacementStatus'])

predicted_salary = loaded_model.predict(input_data)
print("Predicted Salary:", predicted_salary)

