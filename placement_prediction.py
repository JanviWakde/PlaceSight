import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn import preprocessing
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix
from sklearn import metrics
import pickle

from google.colab import drive
drive.mount('/content/drive')

data = pd.read_csv("/content/drive/MyDrive/placement_prediction_data.csv")

print(data)

data.info()

data.shape

data.describe()

"""**Data Cleaning**"""

print(data.isnull().sum())

num_duplicates = data.duplicated().sum()
print(f"Number of duplicate rows: {num_duplicates}")

data.rename(columns={'Communication Skill Rating':'Communication Rating','Workshops/Certificatios':'Workshops','backlogs':'Backlogs','salary':'Salary'},inplace=True)

data = data.loc[:, ~data.columns.str.contains('^Unnamed')]

data

"""**Exploratory Data Analysis**"""

sns.histplot(data["CGPA"], bins=20, kde=True)
plt.title("Distribution of CGPA")
plt.show()

sns.countplot(x="Internship", hue="PlacementStatus", data=data)
plt.title("Internship vs Placement")
plt.show()

sns.countplot(x="Hackathon", hue="PlacementStatus", data=data)
plt.title("Hackathon vs Placement")
plt.show()

X = data.drop(['StudentId','PlacementStatus','Salary'],axis=1)
y = data['PlacementStatus']

le = preprocessing.LabelEncoder()
X['Internship'] = le.fit_transform(X['Internship'])
X['Hackathon'] = le.fit_transform(X['Hackathon'])

y = le.fit_transform(y)

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = 0.3, random_state = 100)

rf = RandomForestClassifier(n_estimators=200, random_state=42)
rf.fit(X_train, y_train)

y_pred = rf.predict(X_test)

print("Accuracy:", accuracy_score(y_test, y_pred))
print(confusion_matrix(y_test, y_pred))
print(classification_report(y_test, y_pred))

with open("model.pkl", "wb") as f:
    pickle.dump(rf, f)

with open('model.pkl', 'rb') as f:
    model = pickle.load(f)

input_data = pd.DataFrame([[8,1,3,2,9,4.8,0,1,71,87,0]],
                          columns=['CGPA', 'Major Projects', 'Workshops', 'Mini Projects',
                                   'Skills', 'Communication Rating', 'Internship', 'Hackathon',
                                   '12th Percentage', '10th Percentage', 'Backlogs'])

prediction = model.predict(input_data)
print("Predicted placement:", prediction)

